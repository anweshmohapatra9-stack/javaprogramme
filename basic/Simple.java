public class Simple{
    public static void main(String[] args) {

        int p = 1000;
        int r = 10;
        int t = 2;

        double si = p * r * t / 100.0;

        System.out.println("principal = " + p);
        System.out.println("rate of interest = " + r);
        System.out.println("time = " + t);
        System.out.println("simple interest = " + si);
    }
}